package model;

public class Vivero {
    private int IDVivero;
    private String UbicacionVivero;
    private String Nombre;

    public int getIDVivero() {
        return IDVivero;
    }

    public void setIDVivero(int IDVivero) {
        this.IDVivero = IDVivero;
    }

    public String getUbicacionVivero() {
        return UbicacionVivero;
    }

    public void setUbicacionVivero(String UbicacionVivero) {
        this.UbicacionVivero = UbicacionVivero;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
}
